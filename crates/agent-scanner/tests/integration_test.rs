/// `ScanSummary::from_results` correctly counts all status categories.
#[test]
fn test_scan_summary_counts_all_status_categories() {
    use agent_scanner::runner::CheckExecutionResult;

    let results = vec![
        CheckExecutionResult {
            result: CheckResult::pass("c1"),
            proof: None,
            duration_ms: 10,
        },
        CheckExecutionResult {
            result: CheckResult::pass("c2"),
            proof: None,
            duration_ms: 15,
        },
        CheckExecutionResult {
            result: CheckResult::fail("c3", "bad"),
            proof: None,
            duration_ms: 20,
        },
        CheckExecutionResult {
            result: CheckResult::error("c4", "boom"),
            proof: None,
            duration_ms: 5,
        },
    ];

    let summary = ScanSummary::from_results(&results, 50);

    assert_eq!(summary.total_checks, 4);
    assert_eq!(summary.passed, 2);
    assert_eq!(summary.failed, 1);
    assert_eq!(summary.errors, 1);
    assert_eq!(summary.skipped, 0);
    assert_eq!(summary.duration_ms, 50);

    // Score = 2 pass / (2 pass + 1 fail) countable = 66.67%
    assert!(
        (summary.score - 66.67).abs() < 0.1,
        "Score should be ~66.67, got {}",
        summary.score
    );
}

