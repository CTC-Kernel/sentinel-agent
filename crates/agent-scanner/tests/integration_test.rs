/// `run_all` executes multiple checks in parallel and returns all results.
#[tokio::test]
async fn test_runner_executes_multiple_checks_in_parallel() {
    let mut registry = CheckRegistry::new();
    for i in 0..8 {
        registry.register(pass_check(&format!("check_{}", i)));
    }
    registry.register(fail_check("check_fail"));

    let config = RunnerConfig {
        check_timeout_ms: 5_000,
        concurrency: 4,
        generate_proofs: true,
    };

    let runner = CheckRunner::new(Arc::new(registry), config);
    let results = runner.run_all().await;

    assert_eq!(results.len(), 9, "All 9 checks should produce results");

    let pass_count = results
        .iter()
        .filter(|r| r.result.status == CheckStatus::Pass)
        .count();
    let fail_count = results
        .iter()
        .filter(|r| r.result.status == CheckStatus::Fail)
        .count();

    assert_eq!(pass_count, 8, "8 passing checks expected");
    assert_eq!(fail_count, 1, "1 failing check expected");
}

