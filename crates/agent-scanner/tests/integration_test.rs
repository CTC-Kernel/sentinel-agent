/// Proof expiration date is approximately `retention_days` in the future.
#[test]
fn test_proof_generator_expiry_matches_retention() {
    use chrono::Utc;
    use uuid::Uuid;

    let retention_days = 30i64;
    let generator = ProofGenerator::with_retention(retention_days);
    let def = CheckDefinitionBuilder::new("exp_check").name("Expiry Check").build();
    let output = CheckOutput::pass("ok", serde_json::json!({}));

    let proof = generator.generate(Uuid::new_v4(), "exp_check", &output, &def);
    let expires = proof.expires_at.expect("Proof must have expiry");

    let days_until_expiry = (expires - Utc::now()).num_days();
    assert!(
        (retention_days - 1..=retention_days).contains(&days_until_expiry),
        "Proof should expire in ~{} days, got {}",
        retention_days,
        days_until_expiry
    );
}

/// HMAC-SHA256 signature can be applied and verified; wrong key fails.
#[test]
fn test_proof_generator_sign_and_verify_signature() {
    use uuid::Uuid;

    let generator = ProofGenerator::new();
    let def = CheckDefinitionBuilder::new("sig_check").name("Sig Check").build();
    let output = CheckOutput::pass("signed", serde_json::json!({}));

    let mut proof = generator.generate(Uuid::new_v4(), "sig_check", &output, &def);
    let key = b"super-secret-32-byte-signing-key";

    // Initially no signature
    assert!(proof.signature.is_none());

    generator.sign(&mut proof, key);
    assert!(
        proof.signature.is_some(),
        "Proof should have a signature after sign()"
    );
    assert!(
        proof
            .signature
            .as_ref()
            .unwrap()
            .starts_with("hmac-sha256:"),
        "Signature must use hmac-sha256 scheme"
    );

    // Correct key verifies
    assert!(
        generator.verify_signature(&proof, key),
        "Correct key must verify the signature"
    );

    // Wrong key does not verify
    assert!(
        !generator.verify_signature(&proof, b"wrong-key-entirely-different"),
        "Wrong key must not verify the signature"
    );
}

/// Proof with no signature returns false from `verify_signature`.
