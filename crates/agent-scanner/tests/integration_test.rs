//! Integration tests for agent-scanner.
//!
//! These tests exercise the public API surface of the three main components:
//!
//! - `CheckRunner` — parallel execution, timeouts, proof generation
//! - `ScoreCalculator` — weighted compliance scoring, valid 0-100 range
//! - `ProofGenerator` — SHA-256 proof hashes, tamper detection, signing
//!
//! No external services or encrypted storage are required; checks are
//! implemented inline as test doubles.

use agent_scanner::{
    check::{Check, CheckDefinitionBuilder, CheckOutput, CheckRegistry},
    error::ScannerResult,
    proof::{ProofGenerator, compute_sha256, verify_sha256},
    runner::{CheckRunner, RunnerConfig, ScanSummary},
    score::{CheckScoreInput, ScoreCalculator, ScoringConfig},
};
use agent_common::types::{CheckCategory, CheckResult, CheckSeverity, CheckStatus};
use async_trait::async_trait;
use std::sync::Arc;
use std::time::Duration;

// ─── Test-double checks ───────────────────────────────────────────────────────

struct PassCheck {
    def: agent_common::types::CheckDefinition,
}

#[async_trait]
impl Check for PassCheck {
    fn definition(&self) -> &agent_common::types::CheckDefinition {
        &self.def
    }
    async fn execute(&self) -> ScannerResult<CheckOutput> {
        Ok(CheckOutput::pass(
            "All good",
            serde_json::json!({"status": "ok"}),
        ))
    }
}

struct FailCheck {
    def: agent_common::types::CheckDefinition,
}

#[async_trait]
impl Check for FailCheck {
    fn definition(&self) -> &agent_common::types::CheckDefinition {
        &self.def
    }
    async fn execute(&self) -> ScannerResult<CheckOutput> {
        Ok(CheckOutput::fail(
            "Non-compliant",
            serde_json::json!({"reason": "policy violation"}),
        ))
    }
}

struct SlowCheck {
    def: agent_common::types::CheckDefinition,
    delay_ms: u64,
}

#[async_trait]
impl Check for SlowCheck {
    fn definition(&self) -> &agent_common::types::CheckDefinition {
        &self.def
    }
    async fn execute(&self) -> ScannerResult<CheckOutput> {
        tokio::time::sleep(Duration::from_millis(self.delay_ms)).await;
        Ok(CheckOutput::pass("Slow pass", serde_json::json!({})))
    }
}

// ─── Builder helpers ──────────────────────────────────────────────────────────

fn pass_check(id: &str) -> Arc<dyn Check> {
    Arc::new(PassCheck {
        def: CheckDefinitionBuilder::new(id)
            .name(format!("Pass Check {}", id))
            .category(CheckCategory::General)
            .build(),
    })
}

fn fail_check(id: &str) -> Arc<dyn Check> {
    Arc::new(FailCheck {
        def: CheckDefinitionBuilder::new(id)
            .name(format!("Fail Check {}", id))
            .category(CheckCategory::General)
            .build(),
    })
}

fn slow_check(id: &str, delay_ms: u64) -> Arc<dyn Check> {
    Arc::new(SlowCheck {
        def: CheckDefinitionBuilder::new(id)
            .name(format!("Slow Check {}", id))
            .build(),
        delay_ms,
    })
}

fn score_input(
    status: CheckStatus,
    severity: CheckSeverity,
    category: &str,
    frameworks: &[&str],
) -> CheckScoreInput {
    let result = match status {
        CheckStatus::Pass => CheckResult::pass("test"),
        CheckStatus::Fail => CheckResult::fail("test", "failed"),
        CheckStatus::Error => CheckResult::error("test", "error"),
        _ => CheckResult::pass("test"),
    };

    CheckScoreInput {
        result,
        severity,
        category: category.to_string(),
        frameworks: frameworks.iter().map(|s| s.to_string()).collect(),
    }
}

