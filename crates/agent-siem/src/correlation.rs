#[cfg(test)]
mod tests {
    use super::*;
    use agent_common::constants::AGENT_VERSION;

    fn make_event(category: EventCategory, severity: u8, description: &str) -> SiemEvent {
        SiemEvent {
            timestamp: Utc::now(),
            severity,
            category,
            name: "test_event".to_string(),
            description: description.to_string(),
            source_host: "test-host".to_string(),
            source_ip: None,
            destination_ip: None,
            destination_port: None,
            user: None,
            process_name: None,
            process_id: None,
            file_path: None,
            custom_fields: serde_json::json!({}),
            event_id: uuid::Uuid::new_v4().to_string(),
            agent_version: AGENT_VERSION.to_string(),
        }
    }

    #[tokio::test]
    async fn test_correlation_engine_no_match() {
        let engine = CorrelationEngine::with_default_rules();
        let event = make_event(EventCategory::System, 2, "routine check passed");
        let alerts = engine.process_event(&event).await;
        assert!(alerts.is_empty());
    }

    #[tokio::test]
    async fn test_brute_force_detection() {
        // Use only the brute_force rule to avoid interference from other rules
        let rules = vec![CorrelationRule {
            id: "brute_force".to_string(),
            name: "Brute Force Detection".to_string(),
            description: "Multiple authentication failures detected".to_string(),
            categories: vec![EventCategory::Authentication],
            pattern: r"(?i)(fail|denied)".to_string(),
            min_severity: 3,
            threshold: 5,
            window_secs: 300,
            alert_severity: 8,
            enabled: true,
        }];
        let engine = CorrelationEngine::new(rules);

        // Simulate 5 failed login events (threshold for brute_force rule)
        for i in 0..5 {
            let event = make_event(
                EventCategory::Authentication,
                5,
                &format!("authentication failed for user test (attempt {})", i + 1),
            );
            let alerts = engine.process_event(&event).await;
            if i < 4 {
                assert!(alerts.is_empty(), "Should not alert before threshold");
            } else {
                assert_eq!(alerts.len(), 1, "Should alert at threshold");
                assert_eq!(alerts[0].rule_id, "brute_force");
                assert_eq!(alerts[0].event_count, 5);
            }
        }
    }

    #[tokio::test]
    async fn test_severity_filter() {
        let engine = CorrelationEngine::with_default_rules();

        // Low severity auth failure should not match brute_force (min_severity=3)
        for _ in 0..10 {
            let event = make_event(EventCategory::Authentication, 1, "login failed");
            let alerts = engine.process_event(&event).await;
            assert!(alerts.is_empty());
        }
    }

    #[tokio::test]
    async fn test_alert_to_event() {
        let engine = CorrelationEngine::with_default_rules();
        let alert = CorrelationAlert {
            rule_id: "test_rule".to_string(),
            rule_name: "Test Rule".to_string(),
            description: "Test alert".to_string(),
            severity: 8,
            event_count: 5,
            first_event: Utc::now(),
            last_event: Utc::now(),
            sample_event_ids: vec!["evt-1".to_string()],
            generated_at: Utc::now(),
        };

        let event = engine.alert_to_event(&alert, "test-host");
        assert_eq!(event.severity, 8);
        assert_eq!(event.category, EventCategory::Security);
        assert!(event.name.contains("test_rule"));
    }

    #[test]
    fn test_default_rules() {
        let rules = default_rules();
        assert!(!rules.is_empty());
        // Verify all patterns compile
        for rule in &rules {
            assert!(Regex::new(&rule.pattern).is_ok(), "Rule '{}' has invalid regex", rule.id);
        }
    }

    #[test]
    fn test_rule_count() {
        let engine = CorrelationEngine::with_default_rules();
        assert!(engine.rule_count() > 0);
    }
}
